                    )
                }
                items(topTripNotes) { tripNote ->
                    PopularTripNoteItem(
                        tripItem = tripNote,
                        onItemClick = { viewModel.onClickTripNote(tripNote.tripNoteDocumentId) }
                    )
                }
            }
        }
    }
}
