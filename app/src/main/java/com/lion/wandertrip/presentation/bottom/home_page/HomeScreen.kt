    val contentsModelMap by viewModel.contentsModelMap.collectAsState()
    // Ï¢ãÏïÑÏöî map Íµ¨ÎèÖ Î≥ÄÏàò
    val favoriteMap by viewModel.favoriteMap.collectAsState()

    LaunchedEffect(Unit) {
        // Ïä§ÌÅ¨Îû© ÎÜíÏùÄ Ïó¨ÌñâÍ∏∞ Í∞ÄÏ†∏Ïò§Í∏∞
        viewModel.getTopScrapedTrips()
        // Ïú†Ï†Ä Ï¢ãÏïÑÏöî Î™©Î°ù content ID Í∞ÄÏ†∏Ïò§Í∏∞
        viewModel.loadFavorites()

    }

    LaunchedEffect(favoriteMap.size) {
        favoriteMap.keys.forEach {
            Log.d("test100", "key: $it")
            viewModel.fetchContentsModel(it)
        }
    }

    val scrollState = rememberScrollState()
    val navController = viewModel.tripApplication.navHostController
    var backStackRoutes by remember { mutableStateOf<List<String>>(emptyList()) }

    LaunchedEffect(navController) {
        navController.currentBackStackEntryFlow.collectLatest { backStackEntry ->
            // ÌòÑÏû¨ Î∞±Ïä§ÌÉùÏùÑ ÏïàÏ†ÑÌïòÍ≤å Í∞ÄÏ†∏Ïò¥
            val backStackList =
                navController.currentBackStack.value.mapNotNull { it.destination.route }

            backStackRoutes = backStackList // ÏµúÏã† Î∞±Ïä§ÌÉù Î∞òÏòÅ
        }
    }

/*
    // Î∞±Ïä§ÌÉù Î°úÍ∑∏ Ï∂úÎ†•
    LaunchedEffect(backStackRoutes) {
        Log.d("BackStack", "===== Current BackStack =====")
        backStackRoutes.forEach { route ->
            Log.d("BackStack", "Route: $route")
        }
        Log.d("BackStack", "=============================")
    }
*/

    if (isLoading) {
        // ‚úÖ Î°úÎî© Ï§ëÏùº Îïå ÌëúÏãúÌï† ÌôîÎ©¥
        LottieLoadingIndicator() // ‚úÖ Î°úÎî© UI ÌëúÏãú
    } else {
        Column(
            modifier = Modifier
                .fillMaxSize()
                .verticalScroll(scrollState)
        ) {
            LazyColumn(
                modifier = Modifier
                    .weight(1f)
                    .padding(16.dp),
                verticalArrangement = Arrangement.spacedBy(16.dp)
            ) {
                item {
                    Row(
                        modifier = Modifier
                            .fillMaxWidth()
                            .height(56.dp), // ÌÉëÎ∞î ÎÜíÏù¥ÏôÄ ÎπÑÏä∑ÌïòÍ≤å ÏÑ§Ï†ïÌïòÏó¨ Í≤ÄÏÉâ ÏïÑÏù¥ÏΩòÏùò ÏàòÏßÅ ÏúÑÏπòÎ•º Ï°∞Ï†ïÌï©ÎãàÎã§.
                        // Ïù¥ÎØ∏ÏßÄÏÉÅ Í≤ÄÏÉâ ÏïÑÏù¥ÏΩò ÏúÑÏóê Ïó¨Î∞±Ïù¥ ÏûàÎäî Í≤É Í∞ôÏúºÎØÄÎ°ú, ÌïÑÏöîÏóê Îî∞Îùº Ïù¥ ÎÜíÏù¥Î•º Ï°∞Ï†àÌïòÍ±∞ÎÇò
                        // Modifier.padding(top = ...)ÏùÑ Ï∂îÍ∞ÄÌï† Ïàò ÏûàÏäµÎãàÎã§.
                        horizontalArrangement = Arrangement.End, // ÏïÑÏù¥ÏΩòÏùÑ Ïö∞Ï∏° ÎÅùÏúºÎ°ú Ï†ïÎ†¨
                        verticalAlignment = Alignment.CenterVertically // ÏïÑÏù¥ÏΩòÏùÑ ÏàòÏßÅ Ï§ëÏïô Ï†ïÎ†¨
                    ) {
                        IconButton(
                            onClick = { viewModel.onClickIconSearch() }
                        ) {
                            Icon(
                                imageVector = Icons.Filled.Search,
                                contentDescription = "Í≤ÄÏÉâ",
                            )
                        }
                    }
                }

                item {
                    HorizontalPopularCityList(viewModel)
                }

                item {
                    Text(
                        text = "Ïù∏Í∏∞ Í¥ÄÍ¥ëÏßÄ",
                        fontSize = 20.sp,
                        fontFamily = NanumSquareRound,
                        modifier = Modifier.padding(bottom = 8.dp)
                    )
                }
                items(popularTripList) { tripItem ->
                    TripSpotItem(
                        tripItem = tripItem,
                        onItemClick = { viewModel.onClickTrip(tripItem.contentId) },
                        userModel = userModel,
                        contentsModel = contentsModelMap[tripItem.contentId],
                        onFavoriteClick = { contentId -> viewModel.toggleFavorite(contentId) },
                        viewModel = viewModel,
                        isFavorite = favoriteMap[tripItem.contentId] ?: false,
                    )
                }
                item {
                    Text(
                        text = "üî• Ïù∏Í∏∞ ÎßéÏùÄ Ïó¨ÌñâÍ∏∞",
                        fontSize = 20.sp,
                        fontFamily = NanumSquareRound,
